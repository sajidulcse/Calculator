    public void arithmetic_operation() {
    try {
        switch (calculation) {
            case 1:  // addition
                ans = num + Double.parseDouble(jTextField1.getText());
                jTextField1.setText(Double.toString(ans));
                break;

            case 2:  // subtraction
                ans = num - Double.parseDouble(jTextField1.getText());
                jTextField1.setText(Double.toString(ans));
                break;

            case 3:  // multiplication
                ans = num * Double.parseDouble(jTextField1.getText());
                jTextField1.setText(Double.toString(ans));
                break;

            case 4:  // division
                double divisor = Double.parseDouble(jTextField1.getText());
                if (divisor != 0) {
                    ans = num / divisor;
                    jTextField1.setText(Double.toString(ans));
                } else {
                    jTextField1.setText("Error: Division by zero");
                }
                break;

            case 5:  // Modulus
                ans = num % Double.parseDouble(jTextField1.getText());
                jTextField1.setText(Double.toString(ans));
                break;

            case 6:  // Square Root
                double inputForSquareRoot = Double.parseDouble(jTextField1.getText());
                if (inputForSquareRoot >= 0) {
                    ans = Math.sqrt(inputForSquareRoot);
                    jTextField1.setText(Double.toString(ans));
                } else {
                    jTextField1.setText("Error: Invalid input for square root");
                }
                break;

            case 7:  // xÂ² (square)
                ans = Math.pow(num, 2);
                jTextField1.setText(Double.toString(ans));
                break;

            case 8:  // ^ (power)
                double power = Double.parseDouble(jTextField1.getText());
                ans = Math.pow(num, power);
                jTextField1.setText(Double.toString(ans));
                break;

            

        }
    } catch (NumberFormatException e) {
        jTextField1.setText("Error: Invalid input");
    } catch (ArithmeticException e) {
        jTextField1.setText("Error: Arithmetic exception - "+ e.getMessage());
    } catch (Exception e) {
        jTextField1.setText("Error: An unexpected error occurred");
    }
}

